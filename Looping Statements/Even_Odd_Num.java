package com.LoopingStatements;

public class Even_Odd_Num 
{
	public static void main(String[] args) 
	{
		System.out.println("Even   Odd");
		for(int i=0;i<=10;i++)
		{
			if(i%2==0)
			{
				System.out.print(i);
			}
			
			else
			{
				System.out.println("       "+i);
				
			}
		}
		
	}

}
