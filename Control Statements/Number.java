package com.ControlStatements;

public class Number 
{
	public static void main(String[] args) 
	{
		int num = 0;
		
		if(num>0)
		{
			System.out.println(num + " is a Positive number.");
		}
		else if(num<0)
		{
			System.out.println(num +" is a negative number.");
		}
		else
		{
			System.out.println(num +" is equal to zero.");
		}
		
			
		
	}

}
